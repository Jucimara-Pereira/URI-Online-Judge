import java.util.Scanner;

public class URI_1001 {
	private static Scanner read;

	public static void main(String[] args) {
		read = new Scanner(System.in);
		
		int A, B, X;
		
		A = read.nextInt();
		B = read.nextInt();
		X = A + B;
		
		System.out.println("X = "+X);
		
	}
}